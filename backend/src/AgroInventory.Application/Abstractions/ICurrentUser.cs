namespace AgroInventory.Application.Abstractions;

/// <summary>
/// Текущий пользователь. В MVP авторизации нет — всегда системный пользователь (ТЗ §6).
/// Абстракция заложена под будущую авторизацию: реализацию заменим, вызовы не изменятся.
/// </summary>
public interface ICurrentUser
{
    Guid UserId { get; }

    /// <summary>
    /// Хозяйство — контекст текущей операции записи (ТЗ §15). Пока авторизации нет,
    /// это дефолтное хозяйство; после этапа B — выбранное хозяйство из HTTP-контекста.
    /// Новые записи справочников/операций штампуются этим CompanyId.
    /// </summary>
    Guid CompanyId { get; }
}
